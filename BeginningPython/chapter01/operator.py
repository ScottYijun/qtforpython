#操作符运算

#加法
print("5 + 8 = ", 5 + 8)

#减法
print("65 - 43 = ", 65 - 43)

#乘法
print("3 * 9 = ", 3 * 9)

#除法
print("36 / 7 = ", 36 / 7)

#整除向下圆整
print("36 // 7 = ", 36 // 7)

#求余
print("10 % 3 = ", 10 % 3)
print("-10 % 3 = ", -10 % 3)
print("10 % -3 = ", 10 % -3)
print("-10 % -3 = ", -10 % -3)

#乘方
print("2 ** 3 = ", 2 ** 3)
print("-3 ** 2 = ", -3 ** 2)
print("(-3) ** 2 = ", (-3) ** 2)