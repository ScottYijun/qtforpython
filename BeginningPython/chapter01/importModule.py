#导入模块
import math
import cmath #复数模块

print("math.floor(32.9) = ", math.floor(32.9))#向下取整
print("int(32.9) = ", int(32.9))#取整
print("math.ceil(32.3) = ", math.ceil(32.3))#向上取整
print("math.ceil(32) = ", math.ceil(32))#向上取整
print("math.sqrt(9) = ", math.sqrt(9))#平方根

print("cmath.sqrt(-1) = ", cmath.sqrt(-1))#平方根
print("(1 + 3j) * (9 + 4j) = ", (1 + 3j) * (9 + 4j))#平方根

